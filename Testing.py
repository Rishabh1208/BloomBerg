def sol(op):

    obstacles = []
    res = ""
    for i in op:
        if len(i) == 2:
            obstacles.append(i[1])
        else:
            flag = 1

            for ob in obstacles:
                if i[1] <= ob and ob <= i[1]+i[2]-1:
                    res += "0"
                    flag = 0
                    break
            if flag:
                res += "1"
    return res


